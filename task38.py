# Display the letter “a” 10 times using the variable defined above.
# NOTE: USE WHILE LOOP 

# Define the variable no_of_times with a value of 10
no_of_times = 10

# Initialize a counter
count = 0

# Use a while loop to display 'a' 10 times
while count < no_of_times:
    print("a")
    count += 1
